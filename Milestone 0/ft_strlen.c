#include <stdio.h>

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i] != '\0')
	{
		i++;
	}
	return (i);
}

int main ()
{
	char s[] = "Messi G.O.A.T";
	printf("%zu", ft_strlen(s));
}